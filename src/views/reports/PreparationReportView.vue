<script setup>
import { ref, computed } from 'vue'
import { FileBarChart, Printer, ClipboardList, Play, AlertTriangle, CheckCircle2, Sparkles } from '@lucide/vue'
import { useStudentStore } from '@/stores/student'
import { useAuthStore } from '@/stores/auth'
import { saathiChat } from '@/services/ollamaService'

const student = useStudentStore()
const auth = useAuthStore()
const grade = computed(() => auth.user?.gradeCode ?? 9)
const board = computed(() => (auth.user?.board ? `${auth.user.board} Board` : 'Balochistan Board') + ' (BISE)')

const pct = (t) => (t.total ? Math.round((t.score / t.total) * 100) : 0)
const passRate = computed(() => student.totalTests ? Math.round((student.passedTests / student.totalTests) * 100) : 0)

const bySubject = computed(() => {
  const map = {}
  for (const t of student.testRecords) {
    const s = t.subject || 'General'
    if (!map[s]) map[s] = { subject: s, tests: 0, sum: 0, best: 0 }
    const p = pct(t)
    map[s].tests++; map[s].sum += p; map[s].best = Math.max(map[s].best, p)
  }
  return Object.values(map)
    .map((m) => {
      const avg = Math.round(m.sum / m.tests)
      return { ...m, avg, status: avg >= 85 ? 'Strong' : avg >= 60 ? 'On track' : 'Needs work' }
    })
    .sort((a, b) => a.avg - b.avg)
})
const weakest = computed(() => bySubject.value.filter((s) => s.avg < 60).slice(0, 4))
const recent = computed(() => student.testRecords.slice(0, 8))
const trend = computed(() => student.chartData.series[0]?.data || [])

function statusBadge(s) { return s === 'Strong' ? 'badge-green' : s === 'On track' ? 'badge-amber' : 'badge-red' }
function barColor(v) { return v >= 85 ? 'var(--t-success)' : v >= 60 ? 'var(--t-gold)' : 'var(--t-danger)' }
function fmtDate(d) { try { return new Date(d).toLocaleDateString('en-PK', { day: 'numeric', month: 'short' }) } catch { return '' } }
function print() { window.print() }

const aiSummary = ref('')
const aiLoading = ref(false)
async function generateSummary() {
  aiLoading.value = true
  const progress = bySubject.value.map((s) => ({ subject: s.subject, tests: s.tests, avg_score: s.avg, best: s.best }))
  const activity = student.testRecords.slice(0, 15).map((t) => ({
    date: (t.date || '').slice(0, 10), subject: t.subject, score: t.score, total: t.total, type: t.type,
  }))
  try {
    aiSummary.value = await saathiChat('exam_readiness_report', grade.value, {
      student_name: auth.user?.name || 'Student', language: 'english',
      progress_json: progress, activity_log_json: activity, exam_json: {}, syllabus_json: {},
      today_date: new Date().toISOString().slice(0, 10),
    }, 'Summarise my exam preparation from my real test history, and tell me the 3 most important next steps.')
  } catch {
    aiSummary.value = 'Could not generate the AI summary right now — please try again.'
  }
  aiLoading.value = false
}
</script>

