        <div class="pr-kpi"><div class="pr-kpi-val">{{ student.avgPercent }}%</div><div class="pr-kpi-lbl">Average score</div></div>
        <div class="pr-kpi"><div class="pr-kpi-val">{{ passRate }}%</div><div class="pr-kpi-lbl">Pass rate</div></div>
